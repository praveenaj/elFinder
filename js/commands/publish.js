elFinder.prototype.commands.publish = function() {
	var fm   = this.fm,
		self = this;
	
	this.getstate = function(sel) {
		var sel = this.files(sel),
			cnt = sel.length;

		return cnt && $.map(sel, function(f) { return f.phash && f.read ? f : null  }).length == cnt ? 0 : -1;
	}
	
	this.exec = function() {
	
	}

}